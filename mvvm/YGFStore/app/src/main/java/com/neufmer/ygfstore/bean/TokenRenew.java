public class TokenRenew {

    /**
     * token : cae9986d-0583-47eb-ae8b-ad68061de937
     * expire : 10800
     */

    private String token;
    private int expire;

    public String getToken() {
        return token;
    }

    public void setToken(String token) {
        this.token = token;
    }

    public int getExpire() {
        return expire;
    }

    public void setExpire(int expire) {
        this.expire = expire;
    }


}
